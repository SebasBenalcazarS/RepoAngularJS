package ec.com.smx.sic.cliente.mdl.dto.id;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

import ec.com.kruger.utilitario.dao.commons.annotations.SequenceDataBaseValue;
import ec.com.smx.corpv2.common.util.CorporativoDescriptorSecuencias;

/**
 * @author jdvasquez
 *
 */
@SuppressWarnings("serial")
@Embeddable
public class ArticuloProcesoID implements Serializable{
	
	/**
	 * Codigo de la compania
	 * 
	 */
	@Column(name = "CODIGOCOMPANIA", nullable = false)
	private Integer codigoCompania;
	
	/**
	 * Especifica el codigo del arituloproceso (secuencia de base)
	 */
	@Column(name="CODIGOARTICULOPROCESO", nullable = false)
	@SequenceDataBaseValue (descriptorClass=CorporativoDescriptorSecuencias.class , name = "SCSPETSECARTPRO")
	private Long codigoProceso;

	/**
	 * @return the codigoCompania
	 */
	public Integer getCodigoCompania() {
		return codigoCompania;
	}

	/**
	 * @param codigoCompania the codigoCompania to set
	 */
	public void setCodigoCompania(Integer codigoCompania) {
		this.codigoCompania = codigoCompania;
	}

	/**
	 * @return the codigoProceso
	 */
	public Long getCodigoProceso() {
		return codigoProceso;
	}

	/**
	 * @param codigoProceso the codigoProceso to set
	 */
	public void setCodigoProceso(Long codigoProceso) {
		this.codigoProceso = codigoProceso;
	}
	
}
